package com.company.model;

public class Albums {
    private int id;
    private String name;
    private int Artist;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getArtist() {
        return Artist;
    }

    public void setArtist(int artist) {
        Artist = artist;
    }
}
